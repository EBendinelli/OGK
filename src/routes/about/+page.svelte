	<h1>About this site</h1>

	<p>
		One night after my partner got gifted Ottolenghi Test Kitchen <a href="https://ottolenghi.co.uk/otk-shelf-love">Shelf Love</a> and  <a href="https://ottolenghi.co.uk/otk-extra-good-things">Extra Good Things</a> for her birthday, we joked with friends that you could probably randomly generate Ottolenghi recipes given how often some ingredients and cooking techniques show up in his cooking. The next day, this site was born.
	</p>

	<p>
		There is absolutely nothing smart about that site, no machine learning, no AI, it's just a bunch of very Ottolenghi vegetables, pulses, carbs, sauces and nothing more that just some randomisation. Code is open source and you're welcome suggest ingredient or changes to the logic of how a recipe is generated on <a href="https://github.com/EBendinelli/OGK">Github</a>.
